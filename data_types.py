# there are a few data types in python that we will cover in this tutorial, including:
# - int: for integers (whole numbers)
# - float: for floating-point numbers (decimal numbers)
# - str: for strings (text)
# - bool: for boolean values (True or False)


# example for each data type: 

# integer (int)
age = 24
type(age)
print (type(age)) # this will return the type of variable age, which is an integer (int)

# float (float)
height = 2.5
type(height)
print (type(height)) # this will return the type of variable height, which is a float

# string (str)
name = 'Python programming'
type(name)
print (type(name)) # this will return the type of variable name, which is a string


# boolean (bool)
is_programming_fun = True #often true or false statements, which 1s a boolean data type
type(is_programming_fun)
print (type(is_programming_fun)) # this will return the type of variable is_programming
